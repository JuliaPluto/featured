md"""## What is dithering?
Dithering is a technique for image quantization, which means that it is used to accurately represent images using much smaller color palettes.

Take the following grayscale image of a lighthouse as an example. We want to display this image using a simple black & white color palette: 
"""

# ‚ïî‚ïê‚ï° 1e9359f2-59c1-4910-b00f-81dc74f889ae
lighthouse = Gray.(testimage("lighthouse")) # convert from color to gray

# ‚ïî‚ïê‚ï° 53d3af8c-88b5-4409-bf6a-9e1b22ac34a0
md"""

### Just black and white
One simple approach is to simply round each pixel up or down to its closest color: `1` for white or `0` for black. **However, a lot of information in the image gets lost!** ü•≤
"""

# ‚ïî‚ïê‚ï° d62c910f-dccd-4976-8d05-efd3e13353d3
map(pixel -> pixel > 0.5 ? Gray(1) : Gray(0), lighthouse)

# ‚ïî‚ïê‚ï° 8a718f57-740b-43a8-9f05-6c699f982220
md"""
### Nice dithering
Dithering solves this problem by adding some pleasant high frequency noise to approximate shading:
"""

# ‚ïî‚ïê‚ï° 0210462f-851b-4e2b-ba99-d7ade531fe11
dither(lighthouse, Atkinson())

# ‚ïî‚ïê‚ï° 7de553b7-e19f-4482-9b5e-d4c8755a033a
md"""
You can see different shades of gray, but this dithered image **only contains black and white pixels**, nothing inbetween! Zoom in to see it better.

> #### Fun facts:
> * The idea behind dithering goes back to an ancient engraving technique from the 15th century called [stippling](https://en.wikipedia.org/wiki/Stippling)!
> * A related concept called [halftoning](https://en.wikipedia.org/wiki/Halftone) is used in printing to approximate grayscale color with black ink. Some dithering algorithms intentionally recreate the effect of halftoning.

You might also have seen retro video games with limited color palettes make use of dithering. It's what gives the [Game Boy Camera](https://en.wikipedia.org/wiki/Game_Boy_Camera) its distinctive look! Let's apply this effect to our own images!
"""

# ‚ïî‚ïê‚ï° 9e1f34a2-c092-4b58-acac-b354f47465e5
