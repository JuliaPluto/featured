# ‚ïî‚ïê‚ï° 582a8cec-fd20-4bf4-babd-cd7f5df53975
md"""If you couldn't guess it's fine, it is not so simple to see at first. 

So, the new value is calculated as the *sum* of:
- The value of the pixel *multiplied* by the center value in the matrix
- The value of the pixel to the top left of the pixel, *multiplied* by the top left value in the filter matrix
- The value of the pixel to to the top, *multiplied* by the center top value in the  filter matrix
- The value of the pixel to the left, *multiplied* by the center left value in the filter matrix
- Etc...

This is effectively what a convolution is! And that's exactly what our code is calculating! """

# ‚ïî‚ïê‚ï° 44d594ff-a25b-4455-be56-863345c67b68
function convolve(img, filter)

	# reads the sizes of the image and the filter
    img_row, img_col = size(img)
    filter_row, filter_col = size(filter)

	# initialize the final image with zeros and the same size as the original image
    filtered = zeros(img_row-filter_row+1, img_col-filter_col+1)
    filtered_row, filtered_col = size(filtered)

	# apply the filter to each pixel in the image by iterating over it, like iterating over a table/2D array/matrix
    for i in 1:filtered_row
        for j in 1:filtered_col
            filtered[i,j] = sum(img[i:i+filter_row-1,j:j+filter_col-1].*filter)
        end
    end
  
    return filtered
end

