Hey everyone,

The [JuliaCon 2024](https://juliacon.org/2024/) Diversity, Equity, and Inclusion (DEI) Committee is excited to announce a **presentation + office hours** event for writing your JuliaCon 2024 proposal! [The call for proposals is out right now](https://pretalx.com/juliacon2024/cfp), and we are looking for both talks and posters for JuliaCon 2024.

